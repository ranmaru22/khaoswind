def pond_interaction():
    return "You hold the mirror up to the pond and you notice that something is moving down there. A strong force is pushing against the old water. You back away, dropping the mirror into the pond. For a second, you feel that whatever lives down almost got you, but then the feeling goes away. When you dare approaching the pond again, you see a spherical object floating on top."
